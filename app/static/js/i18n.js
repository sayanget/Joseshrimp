// 当前语言
let currentLanguage = localStorage.getItem('language') || 'zh';

// 切换语言
function switchLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);

    // 更新显示的语言名称
    const langNames = {
        'zh': '中文',
        'en': 'English',
        'es': 'Español'
    };
    const currentLangElement = document.getElementById('currentLang');
    if (currentLangElement) {
        currentLangElement.textContent = langNames[lang];
    }

    // 翻译页面
    translatePage();

    // 通知服务器（设置session）
    fetch(`/language/set-language/${lang}`)
        .then(() => {
            // 可选：重新加载页面以应用服务器端翻译
            // location.reload();
        });
}

// 翻译页面元素
function translatePage() {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = translations[currentLanguage][key];
        if (translation) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translation;
            } else {
                element.textContent = translation;
            }
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        const translation = translations[currentLanguage][key];
        if (translation) {
            element.placeholder = translation;
        }
    });

    // Translate title attributes (tooltips)
    document.querySelectorAll('[data-i18n-title]').forEach(element => {
        const key = element.getAttribute('data-i18n-title');
        const translation = translations[currentLanguage][key];
        if (translation) {
            element.title = translation;
        }
    });
}

// 页面加载时初始化语言
document.addEventListener('DOMContentLoaded', () => {
    const langNames = {
        'zh': '中文',
        'en': 'English',
        'es': 'Español'
    };
    const currentLangElement = document.getElementById('currentLang');
    if (currentLangElement) {
        currentLangElement.textContent = langNames[currentLanguage] || '中文';
    }
    translatePage();
});

// 导出供其他脚本使用
window.i18n = {
    t: (key) => translations[currentLanguage][key] || key,
    switchLanguage,
    currentLanguage: () => currentLanguage
};
